#include <stdio.h>
#include <string.h>
#include "../includes/aes.h"

int main(int argc, char** argv) {
    if (argc != 2) {
        printf("Usage: ./debugger [FLAG]");
        return 1;
    }

    const uint8_t iv[16] = { 0xa3, 0xaf, 0x24, 0x09, 0x23, 0x0e, 0x80, 0xc3, 0x10, 0xbe, 0x8e, 0x62, 0x09, 0xa9, 0x4a, 0x2e };
    const uint8_t key[32] = { 0xb2, 0x36, 0x67, 0xb7, 0x49, 0x62, 0x29, 0xfc, 0xf2, 0x6a, 0xb9, 0xda, 0x4e, 0x90, 0x39, 0x85, 0xad, 0x60, 0x98, 0x09, 0xe4, 0x47, 0xe9, 0x5d, 0x43, 0x86, 0x9f, 0x7d, 0x54, 0x95, 0x96, 0x22 };
    uint8_t flag_enc[48] = { 0xe3, 0xbc, 0x4a, 0x42, 0x0c, 0x04, 0xd6, 0x58, 0xa1, 0x73, 0xbd, 0x3c, 0xfd, 0x23, 0x5f, 0x43, 0x2a, 0xa3, 0x56, 0x6b, 0x45, 0x35, 0x39, 0xd7, 0x73, 0xff, 0x0d, 0x91, 0x9d, 0x4c, 0xba, 0x9f, 0xed, 0xae, 0x69, 0xc5, 0xcd, 0x48, 0x65, 0x20, 0x55, 0x51, 0x7f, 0xdb, 0x49, 0x39, 0xb2, 0xa1 };

    struct AES_ctx ctx;
    AES_init_ctx_iv(&ctx, key, iv);
    AES_CBC_decrypt_buffer(&ctx, flag_enc, 48);

    char flag[49] = { 0 };
    for (int i = 0; i < 48; i++) {
        flag[i] = (char)flag_enc[i];
    }

    if (strcmp(flag, argv[1]) == 0) {
        printf("That flag is correct!");
    } else {
        printf("That flag is incorrect!");
    }

    return 0;
}
